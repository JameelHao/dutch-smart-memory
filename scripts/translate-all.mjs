async function main() {
  console.log('📚 应用翻译...\n');
  
  // 读取单词文件
  const wordsData = fs.readFileSync(WORDS_FILE, 'utf-8');
  const words = JSON.parse(wordsData);
  
  let translated = 0;
  let skipped = 0;
  
  // 应用翻译
  for (const word of words) {
    if (word.chinese && word.chinese.trim() !== '') {
      skipped++;
      continue;
    }
    
    const dutch = word.dutch.toLowerCase();
    
    if (FULL_TRANSLATIONS[dutch] || FULL_TRANSLATIONS[word.dutch]) {
      word.chinese = FULL_TRANSLATIONS[dutch] || FULL_TRANSLATIONS[word.dutch];
      translated++;
    }
  }
  
  // 保存结果
  fs.writeFileSync(WORDS_FILE, JSON.stringify(words, null, 2));
  
  console.log('✅ 翻译完成！');
  console.log(`   - 新翻译: ${translated}`);
  console.log(`   - 已跳过: ${skipped}`);
  
  // 统计未翻译
  const stillUntranslated = words.filter(w => !w.chinese || w.chinese.trim() === '');
  console.log(`   - 仍未翻译: ${stillUntranslated.length}`);
}

main().catch(console.error);
