async function main() {
  console.log('📚 开始翻译荷兰语单词...\n');
  
  // 读取单词文件
  const wordsData = fs.readFileSync(WORDS_FILE, 'utf-8');
  const words = JSON.parse(wordsData);
  
  console.log(`📊 总共 ${words.length} 个单词\n`);
  
  let translated = 0;
  let skipped = 0;
  let notFound = 0;
  
  // 翻译每个单词
  for (const word of words) {
    if (word.chinese && word.chinese.trim() !== '') {
      skipped++;
      continue;
    }
    
    const dutch = word.dutch.toLowerCase();
    
    if (TRANSLATIONS[dutch]) {
      word.chinese = TRANSLATIONS[dutch];
      translated++;
    } else {
      notFound++;
    }
  }
  
  // 保存结果
  fs.writeFileSync(WORDS_FILE, JSON.stringify(words, null, 2));
  
  console.log('\n✅ 翻译完成！');
  console.log(`   - 已翻译: ${translated}`);
  console.log(`   - 已跳过（已有翻译）: ${skipped}`);
  console.log(`   - 未找到翻译: ${notFound}`);
  console.log(`\n📁 结果已保存到 ${WORDS_FILE}`);
}

main().catch(console.error);
